//Write a program to compare two numbers using >= and <= and log the result to the console.

const operand1 = 20;
const operand2 = 10;

const operand3 = 20;

const result1 = operand1 >= operand3;
const result2 = operand1 <= operand3;

console.log('Result for same value with >= is: ' + result1);
console.log('Result for same value with <= is: ' + result2);

const result3 = operand1 >= operand2;
const result4 = operand1 <= operand2;

console.log('Result for different value with >= is: ' + result3);
console.log('Result for different value with <= is: ' + result4);



